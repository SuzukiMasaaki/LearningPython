print("変数の型を調べる")

c = 1
d = "1"
e = 1.01
f = True
g = [1, 2, 3]
h = (1, 2, 3)

# コメントを挿入するときは「#」を入れるとそこから左に入力したものがコメントになる。
# type()で変数の型を調べることができる
print( type(c) )    #int型
print( type(d) )    #String型
print( type(e) )    #Float型
print( type(f) )    #Boolean型
print( type(g) )    #List型
print( type(h) )    #tuple型(配列っぽい？)
